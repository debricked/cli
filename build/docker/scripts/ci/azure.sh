#!/usr/bin/env bash

# shellcheck disable=SC2034

OWNER=$(basename "${SYSTEM_COLLECTIONURI}")
DEBRICKED_SCAN_REPOSITORY="${OWNER}/${BUILD_REPOSITORY_NAME}"
DEBRICKED_SCAN_COMMIT="${BUILD_SOURCEVERSION}"
DEBRICKED_SCAN_BRANCH="${BUILD_SOURCEBRANCHNAME}"
DEBRICKED_SCAN_PATH="${BUILD_SOURCESDIRECTORY}"
DEBRICKED_SCAN_REPOSITORY_URL="${BUILD_REPOSITORY_URI}"
DEBRICKED_SCAN_INTEGRATION=azureDevOps
if command -v git &> /dev/null
then
  DEBRICKED_SCAN_AUTHOR="$(git log -1 --pretty=%ae)"
fi

